package grouptasks;

public class Fibonacci {

    public static int NthFibonacci(int numOrder) {
        if (numOrder == 1) {
            return 0;
        }
        if (numOrder == 2) {
            return 1;
        }
        if (numOrder == 3) {
            return 1;
        }
        return 0;
    }
}
